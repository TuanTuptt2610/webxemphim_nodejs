import "./userAdminList.css";
import { DataGrid } from "@material-ui/data-grid";
import { DeleteOutline } from "@material-ui/icons";
import { Link } from "react-router-dom";
import { useContext, useEffect } from "react";
import {
  deleteAdmin,
  getAdmins,
} from "../../context/adminContext/AdminApiCalls";
import { AdminContext } from "../../context/adminContext/AdminContext";

export default function UserAdminList() {
  const { users, dispatch } = useContext(AdminContext);

  useEffect(() => {
    getAdmins(dispatch);
  }, [dispatch]);

  const handleDelete = (id) => {
    deleteAdmin(id, dispatch);
  };

  const columns = [
    { field: "_id", headerName: "ID", width: 210 },
    {
      field: "user",
      headerName: "Admin",
      width: 220,
      renderCell: (params) => {
        return (
          <div className="userListUser">
            <img
              className="userListImg"
              src={
                params.row.profilePic ||
                "https://ih0.redbubble.net/image.618427277.3222/flat,1000x1000,075,f.u2.jpg"
              }
              alt=""
            />
            {params.row.username}
          </div>
        );
      },
    },
    { field: "email", headerName: "Email", width: 200 },
    {
      field: "isAdmin",
      headerName: "Is Admin",
      width: 140,
    },
    {
      field: "action",
      headerName: "Action",
      width: 150,
      renderCell: (params) => {
        return (
          <>
            <Link
              to={{ pathname: "/admin/" + params.row._id, user: params.row }}
            >
              <button className="userListEdit">Edit</button>
            </Link>
            <DeleteOutline
              className="userListDelete"
              onClick={() => handleDelete(params.row._id)}
            />
          </>
        );
      },
    },
  ];

  return (
    <div className="userList">
      <DataGrid
        rows={users}
        disableSelectionOnClick
        columns={columns}
        pageSize={8}
        checkboxSelection
        getRowId={(r) => r._id}
      />
    </div>
  );
}
